CREATE TABLE STUDENT (
    ID VARCHAR2(10) PRIMARY KEY,
    NAME VARCHAR2(30) NOT NULL,
    KOR NUMBER(3) DEFAULT 0 CHECK (KOR BETWEEN 0 AND 100), -- 체크 제약조건 0~100
    ENG NUMBER(3) DEFAULT 0 CHECK (ENG BETWEEN 0 AND 100), 
    MATH NUMBER(3) DEFAULT 0 CHECK (MATH BETWEEN 0 AND 100), 
    TOTAL NUMBER(3) DEFAULT 0 CHECK (TOTAL BETWEEN 0 AND 300), 
    AVG NUMBER(5, 2) DEFAULT 0 CHECK (AVG BETWEEN 0 AND 100) --총5자리, 소수점이하 2자리
);

